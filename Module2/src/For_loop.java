
public class For_loop {
	public static void main(String[] args) {
		//For loop
		for (int i=0;i<10;i++)
			System.out.println(i);
		//second example of for loop
		int Sum=0;
		for (int n=0;n<=100;n++) {
			Sum=Sum+n;
			}// end of for loop
		System.out.println("Sum of first 100 numbers is " + Sum);
		}//end of main function
	}// end of class